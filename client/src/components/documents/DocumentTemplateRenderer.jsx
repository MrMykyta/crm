function resolveItemsColumnWidths(columns = []) {
  if (!Array.isArray(columns) || !columns.length) return [];
  const normalized = columns.map((column) => ({
    key: column.key,
    explicit: toNumberOrNull(column.widthPct),
  }));
  const explicitTotal = normalized.reduce((sum, column) => sum + (column.explicit && column.explicit > 0 ? column.explicit : 0), 0);
  const autoCount = normalized.filter((column) => !column.explicit || column.explicit <= 0).length;

  if (autoCount === 0 && explicitTotal > 0) {
    const factor = explicitTotal > 100 ? 100 / explicitTotal : 1;
    return normalized.map((column) => ({
      key: column.key,
      widthPct: clamp(Math.round((column.explicit || 0) * factor * 100) / 100, 4, 90),
    }));
  }

  const remaining = Math.max(0, 100 - Math.min(explicitTotal, 100));
  const autoWidth = autoCount > 0 ? remaining / autoCount : 0;
  return normalized.map((column) => ({
    key: column.key,
    widthPct: clamp(
      Math.round((column.explicit && column.explicit > 0 ? column.explicit : autoWidth) * 100) / 100,
      4,
      90
    ),
  }));
}

function resolveItemsTableMetrics(section = {}) {
  const settings = section?.settings || {};
  const compactRows = settings.compactRows === true;
  const hasDescriptionColumn = settings.showDescription === true;
  const hasRentalPeriod = settings.showPeriod === true;
  const hasAdditionalQty = settings.showAdditionalQty === true;
  const hasVatValue = settings.showVatValue === true;
  const hasDiscount = settings.showDiscount === true;
  const variant = String(section?.variant || settings.tablePreset || "standard");

  let rowHeight = compactRows ? 28 : 36;
  if (hasDescriptionColumn || hasRentalPeriod) rowHeight += compactRows ? 8 : 12;
  if (hasAdditionalQty || hasVatValue || hasDiscount) rowHeight += compactRows ? 4 : 6;
  if (variant === "erp_gray" || variant === "service_rental") rowHeight += compactRows ? 2 : 4;

  return {
    rowHeight,
    titleShellHeight: compactRows ? 46 : 56,
    tableShellHeight: compactRows ? 46 : 56,
    headerHeight: compactRows ? 42 : 50,
  };
}

function buildItemsTableModel(template = null, model = {}, section = {}, languageMode = "pl") {
  void template;
  const settings = section?.settings || {};
  const enabledFields = getEnabledFields(section);
  const normalizedColumns = buildItemsColumns(enabledFields, settings, languageMode);
  const finalWidths = resolveItemsColumnWidths(normalizedColumns);
  const widthMap = new Map(finalWidths.map((entry) => [entry.key, entry.widthPct]));
  const orderedColumns = normalizedColumns.map((column) => ({
    ...column,
    widthPct: widthMap.get(column.key) || null,
  }));
  const visibleColumns = orderedColumns;
  const bodyRowsSource = Array.isArray(model?.items) ? model.items : [];
  const normalizedBodyRows = bodyRowsSource.map((row, rowIndex) => ({
    ...row,
    __rowKey: asText(row?.id, `item-row-${rowIndex}`),
  }));
  const variant = section.variant || settings.tablePreset || "standard";
  const tableWidthPct = clamp(toNumberOrNull(settings.tableWidthPct) ?? 100, 40, 100);

  return {
    sectionKey: section.key,
    variant,
    compactRows: settings.compactRows === true,
    tableWidthPct,
    normalizedColumns,
    visibleColumns,
    orderedColumns,
    finalWidths: orderedColumns.map((column) => ({ key: column.key, widthPct: column.widthPct })),
    headerModel: orderedColumns.map((column) => ({
      key: column.key,
      label: column.label,
      numeric: column.numeric === true,
    })),
    normalizedBodyRows,
    metrics: resolveItemsTableMetrics(section),
    titles: {
      main: documentText("items", languageMode, "Items"),
      continuation: documentText("itemsContinuation", languageMode, "Document items (continued)"),
    },
  };
}

function resolveItemsChunkStaticHeight(tableModel = {}, chunk = {}) {
  const metrics = tableModel?.metrics || resolveItemsTableMetrics();
  const titleHeight = chunk?.showTitle !== false ? metrics.titleShellHeight : 0;
  const headerHeight = chunk?.showHeader !== false ? metrics.headerHeight : 0;
  return titleHeight + metrics.tableShellHeight + headerHeight;
}

function estimateItemsChunkHeight(tableModel = {}, rowCount = 0, chunk = {}) {
  const metrics = tableModel?.metrics || resolveItemsTableMetrics();
  const staticHeight = resolveItemsChunkStaticHeight(tableModel, chunk);
  return staticHeight + Math.max(1, Number(rowCount) || 0) * metrics.rowHeight;
}

function resolveItemsRowsFitCount(tableModel, availableHeight, maxRows, chunk) {
  const rowsLimit = Math.max(0, Number(maxRows) || 0);
  if (rowsLimit < 1) return 0;
  const available = Math.max(0, Number(availableHeight) || 0);
  let low = 1;
  let high = rowsLimit;
  let best = 0;
  while (low <= high) {
    const mid = Math.floor((low + high) / 2);
    const height = estimateItemsChunkHeight(tableModel, mid, chunk);
    if (height <= available) {
      best = mid;
      low = mid + 1;
    } else {
      high = mid - 1;
    }
  }
  return best;
}

function paginateItemsTable(tableModel, pageContext = {}) {
  const firstPageAvailableHeight = Math.max(0, Number(pageContext?.firstPageAvailableHeight) || 0);
  const continuationPageHeight = Math.max(1, Number(pageContext?.continuationPageHeight) || 1);
  const rows = Array.isArray(tableModel?.normalizedBodyRows) ? tableModel.normalizedBodyRows : [];
  const chunks = [];
  let cursor = 0;
  let firstChunkStartsOnNextPage = false;

  const buildChunkMeta = (isContinuation) => ({
    isFirstChunk: !isContinuation,
    isContinuation,
    showMetaLine: isContinuation,
    showTitle: true,
    showHeader: true,
  });

  if (!rows.length) {
    const emptyMeta = buildChunkMeta(false);
    const emptyHeight = estimateItemsChunkHeight(tableModel, 0, emptyMeta);
    return {
      firstChunkStartsOnNextPage: firstPageAvailableHeight < emptyHeight,
      chunks: [
        {
          ...emptyMeta,
          chunkIndex: 0,
          rows: [],
          rowStart: 0,
          rowEnd: -1,
          height: Math.min(continuationPageHeight, emptyHeight),
        },
      ],
    };
  }

  while (cursor < rows.length) {
    const isContinuation = cursor > 0;
    const chunkMeta = buildChunkMeta(isContinuation);
    const availableHeight =
      chunks.length === 0 && !firstChunkStartsOnNextPage
        ? firstPageAvailableHeight
        : continuationPageHeight;
    const minStartHeight = estimateItemsChunkHeight(tableModel, 1, chunkMeta);
    if (availableHeight < minStartHeight) {
      if (chunks.length === 0 && !firstChunkStartsOnNextPage) {
        firstChunkStartsOnNextPage = true;
        continue;
      }
      break;
    }

    const rowsRemaining = rows.length - cursor;
    const fitCount = resolveItemsRowsFitCount(tableModel, availableHeight, rowsRemaining, chunkMeta);
    if (fitCount < 1) break;

    const chunkRows = rows.slice(cursor, cursor + fitCount);
    const chunkHeight = estimateItemsChunkHeight(tableModel, chunkRows.length, chunkMeta);
    chunks.push({
      ...chunkMeta,
      chunkIndex: chunks.length,
      rows: chunkRows,
      rowStart: cursor,
      rowEnd: cursor + chunkRows.length - 1,
      height: Math.min(continuationPageHeight, chunkHeight),
    });
    cursor += chunkRows.length;
  }

  if (cursor < rows.length) {
    let carryCursor = cursor;
    while (carryCursor < rows.length) {
      const isContinuation = carryCursor > 0;
      const chunkMeta = buildChunkMeta(isContinuation);
      const rowsRemaining = rows.length - carryCursor;
      const fitCount = resolveItemsRowsFitCount(tableModel, continuationPageHeight, rowsRemaining, chunkMeta);
      if (fitCount < 1) {
        const singleRow = rows[carryCursor];
        const chunkRows = singleRow ? [singleRow] : [];
        chunks.push({
          ...chunkMeta,
          chunkIndex: chunks.length,
          rows: chunkRows,
          rowStart: carryCursor,
          rowEnd: carryCursor + (chunkRows.length ? 0 : -1),
          height: Math.min(continuationPageHeight, estimateItemsChunkHeight(tableModel, chunkRows.length, chunkMeta)),
        });
        carryCursor += chunkRows.length || 1;
        continue;
      }
      const chunkRows = rows.slice(carryCursor, carryCursor + fitCount);
      chunks.push({
        ...chunkMeta,
        chunkIndex: chunks.length,
        rows: chunkRows,
        rowStart: carryCursor,
        rowEnd: carryCursor + chunkRows.length - 1,
        height: Math.min(continuationPageHeight, estimateItemsChunkHeight(tableModel, chunkRows.length, chunkMeta)),
      });
      carryCursor += chunkRows.length;
    }
  }

  return {
    firstChunkStartsOnNextPage,
    chunks,
  };
}

