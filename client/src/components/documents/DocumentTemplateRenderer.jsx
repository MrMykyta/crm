function getLanguageParts(mode) {
  const normalized = normalizeDocumentLanguageMode(mode, "pl");
  return normalized.split("_").filter(Boolean);
}

function documentText(key, mode, fallback = "") {
  const dict = DOCUMENT_LABELS[key];
  if (!dict) return fallback || key;
  const parts = getLanguageParts(mode);
  const labels = [];
  parts.forEach((part) => {
    const value = String(dict[part] || "").trim();
    if (value && !labels.includes(value)) labels.push(value);
  });
  return labels.join(" / ") || fallback || key;
}

function fieldText(fieldKey, mode, fallback = "") {
  const dict = FIELD_LABELS[fieldKey];
  if (!dict) return fallback || TEMPLATE_FIELD_CATALOG[fieldKey]?.label || fieldKey;
  const parts = getLanguageParts(mode);
  const labels = [];
  parts.forEach((part) => {
    const value = String(dict[part] || "").trim();
    if (value && !labels.includes(value)) labels.push(value);
  });
  return labels.join(" / ") || fallback || fieldKey;
}

