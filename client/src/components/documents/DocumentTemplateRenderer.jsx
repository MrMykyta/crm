function normalizePageMetaPosition(value, fallback = "hidden") {
  const allowed = new Set([
    "header-left",
    "header-center",
    "header-right",
    "footer-left",
    "footer-center",
    "footer-right",
    "hidden",
  ]);
  const normalized = String(value || "").trim().toLowerCase();
  if (allowed.has(normalized)) return normalized;
  return fallback;
}

function normalizeGeneratedByPosition(value, fallback = "hidden") {
  const allowed = new Set(["footer-left", "footer-right", "hidden"]);
  const normalized = String(value || "").trim().toLowerCase();
  if (allowed.has(normalized)) return normalized;
  return fallback;
}

function normalizeMetaVisibility(value, fallback = "repeat-each-page") {
  const normalized = String(value || "").trim().toLowerCase();
  if (META_VISIBILITY_BEHAVIORS.has(normalized)) return normalized;
  return fallback;
}

function formatPageNumberText(formatKey, languageMode, pageIndex, totalPages) {
  const current = pageIndex + 1;
  const total = Math.max(totalPages, 1);
  const format = String(formatKey || "").trim().toLowerCase();
  if (format === "compact") return `${current} / ${total}`;
  if (format === "english-of") return `Page ${current} of ${total}`;
  return `${documentText("page", languageMode, "Page")} ${current}/${total}`;
}

function resolveCompactHeaderMeta(headerSection, model, languageMode, pageIndex) {
  if (!headerSection || pageIndex === 0) return "";
  if (resolveSectionPageBehavior(headerSection) !== "first-page-only") return "";
  if (headerSection?.settings?.repeatMetaOnEachPage !== true) return "";
  const parts = [];
  parts.push(asText(model?.number, "—"));
  parts.push(`${documentText("issueDate", languageMode, "Issue date")}: ${formatDate(model?.issueDate)}`);
  if (asText(model?.statusLabel)) {
    parts.push(`${documentText("status", languageMode, "Status")}: ${asText(model?.statusLabel)}`);
  }
  return parts.filter(Boolean).join(" · ");
}

function resolveFooterMetaSlots(footerSection, model, languageMode, pageIndex, totalPages) {
  const settings = footerSection?.settings || {};
  const enabledFieldKeys = new Set(getEnabledFields(footerSection).map((field) => field.key));
  const footerMetaBehavior = normalizeMetaVisibility(settings.footerMetaBehavior, "repeat-each-page");
  const showFooterMetaLayer = isMetaVisibleByBehavior(footerMetaBehavior, pageIndex, totalPages);
  const pageNumberBehavior = normalizeMetaVisibility(settings.pageNumberBehavior, "repeat-each-page");
    const generatedByBehavior = normalizeMetaVisibility(settings.generatedByBehavior, pageNumberBehavior);

  const showPageNumber =
    showFooterMetaLayer &&
    settings.showPageNumber !== false &&
    enabledFieldKeys.has("footer.pageNumber") &&
    isMetaVisibleByBehavior(pageNumberBehavior, pageIndex, totalPages);
  const showGeneratedBy =
    showFooterMetaLayer &&
    settings.showGeneratedBy !== false &&
    enabledFieldKeys.has("footer.generatedBy") &&
    isMetaVisibleByBehavior(generatedByBehavior, pageIndex, totalPages);

  const pagePosition = normalizePageMetaPosition(
    settings.pageNumberPosition,
    showPageNumber ? "footer-right" : "hidden"
  );
  const generatedPosition = normalizeGeneratedByPosition(
    settings.generatedByPosition,
    showGeneratedBy ? "footer-left" : "hidden"
  );

  const pageText = showPageNumber && pagePosition !== "hidden"
    ? formatPageNumberText(settings.pageNumberFormat, languageMode, pageIndex, totalPages)
    : "";
  const generatedText =
    showGeneratedBy && generatedPosition !== "hidden"
      ? `${documentText("generatedBy", languageMode, "Generated by")}: ${asText(model?.footer?.generatedBy, "CRM")}`
      : "";

  const slots = {
    headerLeft: "",
    headerCenter: "",
    headerRight: "",
    footerLeft: [],
    footerCenter: [],
    footerRight: [],
  };

  if (pageText) {
    if (pagePosition === "header-left") slots.headerLeft = pageText;
    if (pagePosition === "header-center") slots.headerCenter = pageText;
    if (pagePosition === "header-right") slots.headerRight = pageText;
    if (pagePosition === "footer-left") slots.footerLeft.push(pageText);
    if (pagePosition === "footer-center") slots.footerCenter.push(pageText);
    if (pagePosition === "footer-right") slots.footerRight.push(pageText);
  }

  if (generatedText) {
    if (generatedPosition === "footer-left") slots.footerLeft.push(generatedText);
    if (generatedPosition === "footer-right") slots.footerRight.push(generatedText);
  }

  return slots;
}

function isFooterContentVisible(footerSection, pageIndex, totalPages) {
  if (!footerSection?.enabled) return false;
  const settings = footerSection?.settings || {};
  const behavior = normalizeMetaVisibility(settings.footerContentBehavior, "last-page-only");
  if (behavior === "hidden") return false;
  return isMetaVisibleByBehavior(behavior, pageIndex, totalPages);
}

